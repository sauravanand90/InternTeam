/*for-loop:The for loop repeats a block of code a specific number of times. It contains initialization, condition, and increment/decrement in one line.*/
for (let i = 0; i < 5; i++) {
    console.log(i);
    }

/* while-loop: The while loop repeats a block of code while a condition is true. It contains initialization
outside the loop, condition inside the loop, and increment/decrement inside the loop.*/
let i1 = 0;
while (i1 < 5) {
    console.log(i1);
    i1++;
    }
/* do-while-loop: The do-while loop repeats a block of code while a condition is true
It contains initialization outside the loop, increment/decrement inside the loop, and condition inside the loop.
The code block is executed at least once before the condition is checked.*/
let i2 = 0;
do {
    console.log(i2);
    i2++;
    } while (i2 < 5);
    